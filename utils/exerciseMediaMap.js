export function getExerciseDemo(exerciseName) {
  if (!exerciseName) return null;
  const normalized = exerciseName.toLowerCase().trim();
  return exerciseMediaMap[normalized] || null;
}

export function getExerciseColor(exerciseName) {
  const demo = getExerciseDemo(exerciseName);
  return demo?.color || '#64748b';
}

export function getExerciseCategory(exerciseName) {
  const demo = getExerciseDemo(exerciseName);
  return demo?.category || 'exercise';
}

